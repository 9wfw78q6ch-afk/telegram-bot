def fetch_youtube_snapshot(source: SourceDefinition) -> SourceSnapshot:
    ydl_options = {
        "quiet": True,
        "extract_flat": "in_playlist",
        "skip_download": True,
        "noplaylist": False,
    }

    with YoutubeDL(ydl_options) as ydl:
        info = ydl.extract_info(source.url, download=False)

    entries = [entry for entry in (info.get("entries") or []) if entry]
    if not entries:
        raise RuntimeError(f"Na kanálu {source.url} jsem nenašel žádná videa.")

    latest = entries[0]
    url = resolve_video_url(latest)
    title = str(latest.get("title") or "Bez názvu")
    description = str(latest.get("description") or "")
    published_at = normalize_uploaded_at(latest)
    content = description
    fingerprint = latest.get("id") or extract_video_id(url)
    source_name = source.name or title

    return SourceSnapshot(
        source_key=source_key(source),
        source_type="youtube",
        source_name=source_name,
        title=title,
        url=url,
        description=description,
        fingerprint=str(fingerprint),
        published_at=published_at,
        content=content,
    )


def fetch_rss_snapshot(source: SourceDefinition) -> SourceSnapshot:
    response = requests.get(source.url, timeout=20)
    response.raise_for_status()
    root = ET.fromstring(response.text)

    namespace = {
        "atom": "http://www.w3.org/2005/Atom",
        "media": "http://search.yahoo.com/mrss/",
    }

    entry = root.find("channel/item")
    if entry is None:
        entry = root.find("atom:entry", namespace)
    if entry is None:
        raise RuntimeError(f"V RSS/Atom feedu {source.url} jsem nenašel žádnou položku.")

    def first_text(path_options: list[str]) -> str:
        for path in path_options:
            node = entry.find(path, namespace)
            if node is not None and node.text:
                return clean_text(node.text)
        return ""

    title = first_text(["title"]) or "Bez názvu"
    link = first_text(["link"]) 
    if not link:
        link_node = entry.find("link", namespace)
        if link_node is not None:
            link = clean_text(link_node.attrib.get("href", "")) or clean_text(link_node.text or "")

    summary = first_text(["description", "summary", "content", "media:description"])
    published_at = first_text(["pubDate", "published", "updated"])
    guid = first_text(["guid", "id"]) or link or title
    content = summary or title

    return SourceSnapshot(
        source_key=source_key(source),
        source_type="rss",
        source_name=source.name or source.url,
        title=title,
        url=link or source.url,
        description=summary,
        fingerprint=fingerprint_text(guid + "|" + summary),
        published_at=published_at or None,
        content=content,
    )


def fetch_webpage_snapshot(source: SourceDefinition) -> SourceSnapshot:
    response = requests.get(source.url, timeout=20, headers={"User-Agent": "Mozilla/5.0"})
    response.raise_for_status()
    page_html = response.text
    title = extract_html_title(page_html)
    text_content = extract_visible_text(page_html)
    excerpt = text_content[:12000]
    fingerprint = fingerprint_text(excerpt)

    return SourceSnapshot(
        source_key=source_key(source),
        source_type="web",
        source_name=source.name or title,
        title=title,
        url=source.url,
        description="",
        fingerprint=fingerprint,
        published_at=None,
        content=excerpt or title,
    )


def fetch_source_snapshot(source: SourceDefinition) -> SourceSnapshot:
    if source.type == "youtube":
        return fetch_youtube_snapshot(source)
    if source.type in {"rss", "feed", "atom"}:
        return fetch_rss_snapshot(source)
    if source.type in {"web", "website", "page"}:
        return fetch_webpage_snapshot(source)
    raise RuntimeError(f"Nepodporovaný typ zdroje: {source.type}")


def resolve_video_url(entry: dict[str, Any]) -> str:
    video_id = entry.get("id") or entry.get("url") or ""
    if isinstance(video_id, str) and re.fullmatch(r"[A-Za-z0-9_-]{11}", video_id):
        return f"https://www.youtube.com/watch?v={video_id}"

    url = entry.get("webpage_url") or entry.get("url") or ""
    if isinstance(url, str) and url.startswith("http"):
        return url

    if isinstance(video_id, str) and video_id:
        return f"https://www.youtube.com/watch?v={video_id}"

    raise RuntimeError("Nepodařilo se zkonstruovat URL videa z dat kanálu.")


