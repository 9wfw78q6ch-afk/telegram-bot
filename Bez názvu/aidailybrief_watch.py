def fetch_subtitles_with_ytdlp(video_url: str) -> str:
    with tempfile.TemporaryDirectory(prefix="aidailybrief-subs-") as temp_dir_name:
        temp_dir = Path(temp_dir_name)
        ydl_options = {
            "quiet": True,
            "skip_download": True,
            "writeautomaticsub": True,
            "writesubtitles": True,
            "subtitlesformat": "vtt",
            "subtitleslangs": ["en", "en-US", "en-GB", "cs", "cs-CZ", "sk"],
            "outtmpl": str(temp_dir / "%(id)s.%(ext)s"),
        }

        with YoutubeDL(ydl_options) as ydl:
            ydl.extract_info(video_url, download=True)

        subtitle_files = sorted(temp_dir.glob("*.vtt"))
        if not subtitle_files:
            raise RuntimeError("Nepodařilo se stáhnout automatické titulky přes yt-dlp.")

        lines: list[str] = []
        for raw_line in subtitle_files[0].read_text(encoding="utf-8", errors="ignore").splitlines():
            line = raw_line.strip()
            if not line:
                continue
            if line == "WEBVTT" or line.startswith("NOTE"):
                continue
            if "-->" in line:
                continue
            if re.fullmatch(r"\d+", line):
                continue
            line = re.sub(r"<[^>]+>", "", line)
            if lines and lines[-1] == line:
                continue
            lines.append(line)

        transcript = "\n".join(lines).strip()
        if not transcript:
            raise RuntimeError("Stažené titulky jsou prázdné.")
        return transcript


