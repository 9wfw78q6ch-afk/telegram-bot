def strip_weekly_preamble(text: str) -> str:
    cleaned = text.strip()
    if not cleaned:
        return cleaned

    lines = [line.strip() for line in cleaned.splitlines() if line.strip()]
    if not lines:
        return cleaned

    first_line = lines[0].lower()
    if any(first_line.startswith(prefix) for prefix in WEEKLY_INTRO_PHRASES):
        lines = lines[1:]

    cleaned = "\n".join(lines).strip()
    cleaned = re.sub(
        r"^(?:here is(?: a(?: concise and accurate)?)? summary of the text(?: in [^:]+)?|here are(?: the key takeaways| the main points| 5-8 key takeaways)?(?: from the text)?(?: in [^:]+)?)\s*[:\-–—]?\s*",
        "",
        cleaned,
        flags=re.IGNORECASE,
    )
    cleaned = re.sub(
        r"^(?:summary|shrnutí|textový shrnutí|shrnutí textu)\s*[:\-–—]\s*",
        "",
        cleaned,
        flags=re.IGNORECASE,
    )
    return cleaned.strip()


def clean_model_summary(text: str) -> str:
    cleaned = strip_weekly_preamble(text)
    return cleaned or text.strip()


def normalize_weekly_summary(summary: str, max_points: int = 7) -> str:
    cleaned = strip_weekly_preamble(summary)
    if not cleaned:
        return "- Shrnutí se nepodařilo vygenerovat."

    candidate_lines = [line.strip() for line in cleaned.splitlines() if line.strip()]
    points: list[str] = []

    def add_point(value: str) -> None:
        value = re.sub(r"^[\-•*\d\.)\s]+", "", value).strip()
        value = re.sub(
            r"^(?:here is(?: a(?: concise and accurate)?)? summary of the text(?: in [^:]+)?|here are(?: the key takeaways| the main points| 5-8 key takeaways)?(?: from the text)?(?: in [^:]+)?)\s*[:\-–—]?\s*",
            "",
            value,
            flags=re.IGNORECASE,
        ).strip()
        value = re.sub(r"^(?:summary|shrnutí|textový shrnutí|shrnutí textu)\s*[:\-–—]\s*", "", value, flags=re.IGNORECASE).strip()
        if value and value not in points:
            points.append(value)

    for line in candidate_lines:
        if re.match(r"^[\-•*\d\.)]\s*", line):
            if not is_bad_summary_output(line):
                add_point(line)
            continue
        if len(candidate_lines) == 1:
            for sentence in re.split(r"(?<=[.!?])\s+", line):
                sentence = sentence.strip()
                if sentence and not is_bad_summary_output(sentence):
                    add_point(sentence)
        else:
            if not is_bad_summary_output(line):
                add_point(line)

    if not points:
        for sentence in re.split(r"(?<=[.!?])\s+", cleaned):
            sentence = sentence.strip()
            if sentence and not is_bad_summary_output(sentence):
                add_point(sentence)

    if not points:
        return "- Shrnutí se nepodařilo vygenerovat."

    points = points[:max_points]
    return "\n".join(f"- {point}" for point in points)


def summary_to_complete_sentences(summary: str, max_sentences: int = 2) -> str:
    cleaned = strip_weekly_preamble(summary)
    if is_bad_summary_output(cleaned):
        return ""

    sentences = [sentence.strip() for sentence in re.split(r"(?<=[.!?])\s+", cleaned) if sentence.strip()]
    if not sentences:
        return ""

    result_sentences: list[str] = []
    for sentence in sentences:
        sentence = re.sub(r"^[\-•*\d\.)\s]+", "", sentence).strip()
        if sentence:
            result_sentences.append(sentence)
        if len(result_sentences) >= max_sentences:
            break

    if not result_sentences:
        return ""

    return " ".join(result_sentences)


def sanitize_summary_for_weekly(summary: str, max_sentences: int = 2) -> str:
    return summary_to_complete_sentences(summary, max_sentences=max_sentences)


def summarize_text_with_model(
    text: str,
    settings: Settings,
    system_prompt: str,
    user_prompt: str,
) -> str:
    if settings.openai_api_key:
        try:
            from openai import OpenAI

            client = OpenAI(api_key=settings.openai_api_key)
            response = client.chat.completions.create(
                model=settings.summary_model,
                messages=[
                    {"role": "system", "content": system_prompt},
                    {"role": "user", "content": f"{user_prompt}\n\n{text}"},
                ],
            )
            content = str(response.choices[0].message.content).strip()
            if content and not is_bad_summary_output(content):
                return clean_model_summary(content)
            if content:
                print("⚠ OpenAI vrátila nekvalitní shrnutí, používám fallback.")
        except Exception as exc:
            print(f"⚠ OpenAI shrnutí selhalo, přecházím na Ollamu: {exc}")

    if settings.nvidia_api_key:
        try:
            response = requests.post(
                f"{settings.nvidia_base_url.rstrip('/')}/chat/completions",
                headers={
                    "Authorization": f"Bearer {settings.nvidia_api_key}",
                    "Content-Type": "application/json",
                },
                json={
                    "model": settings.nvidia_model,
                    "messages": [
                        {"role": "system", "content": system_prompt},
                        {"role": "user", "content": f"{user_prompt}\n\n{text}"},
                    ],
                    "temperature": 0.2,
                    "top_p": 0.9,
                    "max_tokens": 900,
                },
                timeout=settings.nvidia_timeout_seconds,
            )
            response.raise_for_status()
            payload = response.json()
            choices = payload.get("choices") or []
            content = str(((choices[0] if choices else {}) or {}).get("message", {}).get("content") or "").strip()
            if content and not is_bad_summary_output(content):
                return clean_model_summary(content)
            if content:
                print("⚠ NVIDIA vrátila nekvalitní shrnutí, používám fallback.")
        except Exception as exc:
            print(f"⚠ NVIDIA shrnutí selhalo, přecházím na Ollamu: {exc}")

    try:
        response = requests.post(
            f"{settings.ollama_base_url.rstrip('/')}/api/chat",
            json={
                "model": settings.ollama_model,
                "messages": [
                    {"role": "system", "content": system_prompt},
                    {"role": "user", "content": f"{user_prompt}\n\n{text}"},
                ],
                "stream": False,
                "options": {"temperature": 0.2},
            },
            timeout=settings.ollama_timeout_seconds,
        )
        response.raise_for_status()
        payload = response.json()
        content = str((payload.get("message") or {}).get("content") or "").strip()
        if content and not is_bad_summary_output(content):
            return clean_model_summary(content)
        if content:
            print("⚠ Ollama vrátila nekvalitní shrnutí, používám fallback.")
        raise RuntimeError("Ollama vrátila prázdné shrnutí.")
    except Exception as exc:
        print(f"⚠ Ollama shrnutí selhalo, používám lokální heuristiku: {exc}")
        return fallback_summary(text)


def compact_summary(summary: str, max_lines: int = 3, max_chars: int = 320) -> str:
    lines = [line.strip() for line in summary.splitlines() if line.strip()]
    cleaned: list[str] = []

    for line in lines:
        line = re.sub(r"^[\-•\*\d\.)\s]+", "", line).strip()
        if line:
            cleaned.append(line)
        if len(cleaned) >= max_lines:
            break

    if not cleaned:
        cleaned = [summary.strip()]

    short_text = " ".join(cleaned)
    if len(short_text) > max_chars:
        short_text = short_text[: max_chars - 1].rstrip() + "…"

    return short_text


