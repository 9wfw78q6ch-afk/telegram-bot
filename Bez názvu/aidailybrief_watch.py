def run_once(settings: Settings, test_mode: bool = False, test_subject: str = "") -> int:
    try:
        message = process_test_run(settings, test_subject) if test_mode else process_all_sources(settings)
        print(message)
        return 0
    except Exception as exc:
        print(f"Chyba: {exc}")
        return 1


def run_daemon(settings: Settings) -> int:
    interval_seconds = max(1, settings.interval_hours * 60 * 60)
    print(
        f"Spouštím nepřetržité sledování zdrojů ze {settings.sources_file} každých {settings.interval_hours} hodin."
    )

    while True:
        started_at = datetime.now(timezone.utc)
        print(f"[{started_at.isoformat()}] Kontrola nového videa...")
        exit_code = run_once(settings)
        print(f"[{datetime.now(timezone.utc).isoformat()}] Další kontrola za {settings.interval_hours} hodin.")
        if exit_code != 0:
            print("Běh pokračuje i po chybě, aby se další kontrola nespustila až po ručním zásahu.")
        time.sleep(interval_seconds)

    return 0


