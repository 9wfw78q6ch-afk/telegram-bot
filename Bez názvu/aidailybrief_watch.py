def send_telegram_text(bot_token: str, chat_id: str, message: str) -> None:
    if not bot_token or not chat_id or not message:
        return

    try:
        response = post_telegram_message(bot_token, chat_id, message)
        if response.status_code == 200:
            return

        if response.status_code == 400:
            try:
                payload = response.json()
            except Exception:
                payload = {}

            migrated_chat_id = payload.get("parameters", {}).get("migrate_to_chat_id")
            if migrated_chat_id:
                migrated_response = post_telegram_message(bot_token, str(migrated_chat_id), message)
                if migrated_response.status_code == 200:
                    print(f"⚠ Telegram chat byl migrován na nové ID {migrated_chat_id}; zpráva byla odeslána na nové ID.")
                    return
                print(f"⚠ Telegram notifikace selhala i po migraci: {migrated_response.status_code} - {migrated_response.text}")
                return

        print(f"⚠ Telegram notifikace selhala: {response.status_code} - {response.text}")
    except Exception as exc:
        print(f"⚠ Chyba při odesílání Telegram notifikace: {exc}")


