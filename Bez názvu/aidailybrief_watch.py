def build_telegram_update_message(update: UpdateRecord, position: int | None = None, total: int | None = None) -> str:
    header_bits = ["<b>Novinka</b>"]
    if position is not None and total is not None and total > 1:
        header_bits.append(f"{position}/{total}")

    lines = [" · ".join(header_bits), ""]
    lines.extend(
        [
            f"<b>{telegram_escape(update.source_name)}</b>",
            f"<a href=\"{telegram_escape(update.url)}\">{telegram_escape(update.title)}</a>",
            f"{telegram_escape(compact_summary(update.summary, max_lines=4, max_chars=900))}",
            f"<i>{telegram_escape(update.source_type)}</i> · {telegram_escape(update.report_path.name)}",
            "<i>Zpracováno automatizací</i>",
        ]
    )
    return "\n".join(lines).strip()


