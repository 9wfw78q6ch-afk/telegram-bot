def is_bad_summary_output(text: str) -> bool:
    cleaned = text.strip().lower()
    if not cleaned:
        return True

    if any(phrase in cleaned for phrase in BAD_SUMMARY_PHRASES):
        return True

    if re.match(r"^(here is|here are)\b", cleaned):
        return True

    if re.match(r"^(const\s|function\s|document\.|window\.|<html|<!doctype|import\s)", cleaned):
        return True

    if cleaned.startswith("\"") or cleaned.startswith("'"):
        return True

    if len(cleaned) < 20:
        return True

    return False


