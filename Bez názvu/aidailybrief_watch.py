def heuristic_summary(transcript: str, max_sentences: int = 6) -> str:
    sentences = [sentence.strip() for sentence in re.split(r"(?<=[.!?])\s+", transcript) if sentence.strip()]
    if not sentences:
        return "- Přepis je prázdný nebo se nepodařilo text rozdělit na věty."

    word_counts: Counter[str] = Counter()
    for sentence in sentences:
        for word in re.findall(r"[A-Za-z']+", sentence.lower()):
            if word not in STOPWORDS:
                word_counts[word] += 1

    if not word_counts:
        selected = sentences[:max_sentences]
    else:
        ranked_sentences = sorted(
            (
                (sum(word_counts[word] for word in re.findall(r"[A-Za-z']+", sentence.lower()) if word not in STOPWORDS), index, sentence)
                for index, sentence in enumerate(sentences)
            ),
            reverse=True,
        )
        selected = [sentence for _, _, sentence in sorted(ranked_sentences[:max_sentences], key=lambda item: item[1])]

    return "\n".join(f"- {sentence}" for sentence in selected)


