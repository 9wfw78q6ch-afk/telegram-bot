def main() -> int:
    args = parse_args()
    settings = load_settings()

    if args.interval_hours is not None:
        settings = Settings(
            sources_file=settings.sources_file,
            output_dir=settings.output_dir,
            state_file=settings.state_file,
            interval_hours=args.interval_hours,
            summary_model=settings.summary_model,
            transcription_model=settings.transcription_model,
            nvidia_base_url=settings.nvidia_base_url,
            nvidia_model=settings.nvidia_model,
            nvidia_timeout_seconds=settings.nvidia_timeout_seconds,
            ollama_base_url=settings.ollama_base_url,
            ollama_model=settings.ollama_model,
            ollama_timeout_seconds=settings.ollama_timeout_seconds,
            openai_api_key=settings.openai_api_key,
            nvidia_api_key=settings.nvidia_api_key,
            telegram_bot_token=settings.telegram_bot_token,
            telegram_chat_id=settings.telegram_chat_id,
            telegram_admins=settings.telegram_admins,
        )

    if args.test:
        return run_once(settings, test_mode=True, test_subject=args.test_subject)

    if args.weekly_report:
        try:
            message = process_weekly_report(settings)
            print(message)
            return 0
        except Exception as exc:
            print(f"Chyba při týdenním reportu: {exc}")
            return 1

    if args.daily_usecase:
        try:
            message = process_daily_usecase(settings)
            print(message)
            return 0
        except Exception as exc:
            print(f"Chyba při denním usecase: {exc}")
            return 1

    if args.daemon:
        return run_daemon(settings)

    if getattr(args, "telegram_poller", False):
        try:
            poll_telegram_commands(settings)
            return 0
        except KeyboardInterrupt:
            print("Telegram poller byl přerušen uživatelem.")
            return 0

    return run_once(settings)


