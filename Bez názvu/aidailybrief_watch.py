def pending_updates_key() -> str:
    return "pending_updates"


def load_pending_updates(state: dict[str, Any]) -> list[UpdateRecord]:
    raw_pending = state.get(pending_updates_key(), [])
    if not isinstance(raw_pending, list):
        return []

    pending: list[UpdateRecord] = []
    for raw_item in raw_pending:
        if not isinstance(raw_item, dict):
            continue
        update = update_from_payload(raw_item)
        if update is not None:
            pending.append(update)
    return pending


def save_pending_updates(state: dict[str, Any], updates: list[UpdateRecord]) -> None:
    state[pending_updates_key()] = [normalize_update_payload(update) for update in updates]


def merge_updates(existing: list[UpdateRecord], new_items: list[UpdateRecord]) -> list[UpdateRecord]:
    seen: set[str] = set()
    merged: list[UpdateRecord] = []

    for item in [*existing, *new_items]:
        key = item.fingerprint or item.url or item.title
        if key in seen:
            continue
        seen.add(key)
        merged.append(item)

    return merged


