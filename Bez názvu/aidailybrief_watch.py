def load_state(state_file: Path) -> dict[str, Any]:
    if not state_file.exists():
        return {}
    return json.loads(state_file.read_text(encoding="utf-8"))


def save_state(state_file: Path, payload: dict[str, Any]) -> None:
    state_file.parent.mkdir(parents=True, exist_ok=True)
    state_file.write_text(json.dumps(payload, ensure_ascii=False, indent=2), encoding="utf-8")


def load_sources(settings: Settings) -> list[SourceDefinition]:
    if settings.sources_file.exists():
        raw_sources = json.loads(settings.sources_file.read_text(encoding="utf-8"))
        if not isinstance(raw_sources, list):
            raise RuntimeError("SOURCES_FILE musí obsahovat JSON pole zdrojů.")

        sources: list[SourceDefinition] = []
        for raw_source in raw_sources:
            if not isinstance(raw_source, dict):
                continue
            source_type = str(raw_source.get("type") or "").strip().lower()
            source_url = str(raw_source.get("url") or "").strip()
            source_name = str(raw_source.get("name") or "").strip() or None
            priority_value = raw_source.get("priority")
            if not source_type or not source_url:
                continue
            try:
                priority = int(priority_value) if priority_value is not None else default_priority_for_type(source_type)
            except (TypeError, ValueError):
                priority = default_priority_for_type(source_type)
            sources.append(SourceDefinition(type=source_type, url=source_url, name=source_name, priority=priority))

        if not sources:
            raise RuntimeError(f"{settings.sources_file} neobsahuje žádné platné zdroje.")
        return sorted(sources, key=source_sort_key, reverse=True)

    return [
        SourceDefinition(
            type="youtube",
            url=os.getenv("YOUTUBE_CHANNEL_URL", DEFAULT_CHANNEL_URL),
            name="AIDailyBrief",
            priority=default_priority_for_type("youtube"),
        )
    ]


def default_priority_for_type(source_type: str) -> int:
    if source_type == "youtube":
        return 3
    if source_type in {"rss", "atom", "feed"}:
        return 3
    if source_type in {"web", "website", "page"}:
        return 1
    return 0


def source_sort_key(source: SourceDefinition) -> tuple[int, str, str]:
    return (source.priority, source.name or source.url, source.url)


def source_key(source: SourceDefinition) -> str:
    normalized_name = re.sub(r"[^a-z0-9]+", "-", (source.name or source.url).lower()).strip("-")
    normalized_url = hashlib.sha256(source.url.encode("utf-8")).hexdigest()[:8]
    return f"{source.type}:{normalized_name}:{normalized_url}"


